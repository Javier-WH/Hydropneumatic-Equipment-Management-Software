package actors;

public class User {
	
	String id = "";
	String equipament = "";
	String mantenanceBoss = "";
	String operator = "";
	String controlNumber = "";
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getEquipament() {
		return equipament;
	}
	public void setEquipament(String equipament) {
		this.equipament = equipament;
	}
	public String getMantenanceBoss() {
		return mantenanceBoss;
	}
	public void setMantenanceBoss(String mantenanceBoss) {
		this.mantenanceBoss = mantenanceBoss;
	}
	public String getOperator() {
		return operator;
	}
	public void setOperator(String operator) {
		this.operator = operator;
	}
	public String getControlNumber() {
		return controlNumber;
	}
	public void setControlNumber(String controlNumber) {
		this.controlNumber = controlNumber;
	}
	
	
	
	
}
